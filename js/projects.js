const datas = Object.entries(countries);

const travelCity  = urlParams.get('travel_city');    
const travelCountry  = urlParams.get('travel_country');  

var headerOutput;
var worldHref = '<a href="projects.html">Talbots</a>';
var countryHref = '<a href="projects.html?travel_country=' + travelCountry + '">' + travelCountry +'</a>';
var cityHref = '<a href="projectsContent.html?travel_country=' + travelCountry + '&travel_city='+travelCity+'">'+travelCity+'</a>';
if (travelCountry != null && travelCity != null)
{
    headerOutput = worldHref + " --> " + countryHref + " --> " + cityHref;
}
else if(travelCountry != null)
{
    headerOutput = worldHref + " --> " + countryHref;
}
else
{
    headerOutput = worldHref;
}

// Render the image for the projects.html page.
if(page == 'projects.html') {
    let output  = '';
    var countriesDisplayed = new Set()
    for(let key of datas) 
    {
        var cityName = key[0];
        var cityDesc = key[1]["Desc"];
        var cityImag = key[1]["Images"][0];
        var cityCountry = key[1]["Country"];

        // Level 1. Show all Countries
        if (travelCountry == null)      
        {
            //Display a country on Main page only once
            if(countriesDisplayed.has(cityCountry))
                continue;
            else
                countriesDisplayed.add(cityCountry);

            output += `
            <a href="projects.html?travel_country=${cityCountry}" class="item">
                <div class="img-container">
                    <img src="${cityImag}" alt="">
                </div>
                <span>${cityCountry}</span>
            </a>
            `;
        }
        // Level 2: Show only a specific Country
        else
        {
            if (travelCountry != cityCountry)
            {
                continue;
            }
            output += `
            <a href="projectsContent.html?travel_country=${cityCountry}&travel_city=${cityName}" class="item">
                <div class="img-container">
                    <img src="${cityImag}" alt="">
                </div>
                <span>${cityName}</span>
            </a>
            `;
        }
    }

    // Create element
    const div     = document.createElement("div");
    div.className = 'travel-items';
    div.innerHTML = output;
    document.querySelector(".travel-container").insertAdjacentElement("beforeend", div);
    document.querySelector(".travel-header").innerHTML = headerOutput;
}
else
{
    // Level 3: Show photos for a Country, city
    let output=  '';
    for(let key of datas) 
    {
        var cityName = key[0];
        var cityDesc = key[1]["Desc"];
        var cityImages = key[1]["Images"];

        if (cityName != travelCity)
            continue;

        document.querySelector(".travel-description").innerHTML= cityDesc;

        var videoFound = false;    
        for (let cityImag of cityImages) 
        {
            if (videoFound)
            {
                output += `   
                <a href="${videoName}" class='grid-item'>
                    <img src="${cityImag}">
                </a>   
                `;
                videoFound = false;
            }
            else
            {
                //If Video use next image in array as Video Thumbnail
                if (cityImag.includes('.mp4'))
                {
                    videoFound = true;
                    videoName = cityImag;
                }
                else
                {
                    output += `
                    <a href="${cityImag}" class='grid-item' data-fancybox="gallery">
                        <img src="${cityImag}">
                    </a>
                    `;
                }
            }
        }
    }

    // Create and Inject new Div
    const parentDiv = document.createElement("div");
    parentDiv.className   = 'grid';
    parentDiv.innerHTML= `
        <div class="grid-col grid-col--1"></div>
        <div class="grid-col grid-col--2"></div>
        ${output}
    `;

    document.querySelector(".travel-container").insertAdjacentElement("beforeend", parentDiv);
    document.querySelector(".travel-header").innerHTML= headerOutput;
    document.querySelector(".travel-header").style.textTransform = 'capitalize';

}